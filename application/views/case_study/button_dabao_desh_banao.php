							<h5>BRIEF:</h5>
							<p>The Right to Vote is a right given to the people by democracy. It’s a responsibility of every citizen to vote in elections. It’s our right to determine who will govern the country. Network18 take up this initiative for encouraging voters to exercise their right in general elections. This initiative was executed with an idea of <b>#ButtonDabaoDeshBanao</b> </p>
							<h5>OBSTACLES:</h5>
							<p>We ourselves create a lot of obstacles in forming our government by not casting vote as it may be because of the age, social, race and economic class.</p>
							<p>Young generation are often or too busy and have little stake for elections.</p>
							<p>People are showing apathy towards elections as they find no difference by changing government. Hence, they seem alienated when it comes for voting.</p>
							<h5>OBJECTIVES:</h5>
							<p>The sole purpose was to encourage the young, first time voters and to the people who don’t cast their vote as a right. Our mission was to persuade such voters to cast their vote and utilize their right. We as a social media marketing agency want to induce the importance of ‘Right to Vote’ mentioned in Indian Constitution.</p>
							<h5>CHALLENGES:</h5>
							<p>Our Creative and content team deemed to attract the young voters who were voting for the first time and the voters who do not vote on the given holiday for the same. Also, everyone else on the different media outlets was creating awareness regarding right to vote. Hence, our digital marketing agency needs to stand different from them and need to create a unique path which will make voters realize their importance of voting.</p>
							<h5>SOLUTION:</h5>
							<p>
								Our solution includes the <b>#ButtonDabaoDeshBanao</b>. Under this hash tag we went from creating different ideas with fillers, memes, campaigns, factoids, countdowns and many more. The ideas include creative such as-
							</p>
							<ul>
								<li>Longer the Line, Stronger the Nation</li>
								<li>Kab Aur Kahan</li>
								<li>Why 18 is Special</li>
								<li>United We Stand- joining hands</li>
								<li>Together We Elect Our leader</li>
								<li>Did you know</li>
								<li>Service voters</li>
								<li>Be a Game Changer</li>
							</ul>
							<h5>EXECUTION:</h5>
							<p>Our campaign started with the #ButtonDabaoDeshBanao which started from the tenure from 9 th April to 20 th May, Lok Sabha 2019 that endeavors to make people cast their vote. The campaign was highlighted by Network18’s extensive television channels, social media handles, Print and other OTT platforms. The reach was entirely performed by Network18 group. The campaign adds ups:</p>
							<h5>Daily designs and memes on Social media Handles:</h5>
							<p>‘Make your Vote your Voice’ clips were uploaded on daily basis on all social media handles and similarly in other languages including Hindi under the name of ‘Apne vote ko apni Awaaz banao’. Similar short videos were added as ‘Longer the line, stronger the nation’ to encourage people for voting. It also includes different memes and other creative designs.</p>

