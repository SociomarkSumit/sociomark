							<h5>OBSTACLES:</h5>
							<p>
								Our primary obstacle was the initiative itself which was very common. There were many institutions and organizations which were promoting the same initiative in different ways and hence we needed to stand out differently from them. Also, we want to add the best out of the box idea that will highlight the same initiative in such a way that the audience must not only remember this road safety initiative but also follow it in the manner we want.
							</p>
							<h5>OBJECTIVES:</h5>
							<p>As per 2014 reports, India used to hold 12% of global road accidents and 72% victims which were involved in such mishaps was among the age group of 15-44 years where speed, reckless and drunk driving itself are responsible for 1.5% of road accidents.</p>
							<p>Our objective was to create awareness regarding road safety with all the minutes associated with road safety in such a way that we can attract our target audience. Our target audience was among age 18- 45 but mostly the young drivers. Our sole purpose was to shift the only drivers to responsible drivers. Along with it, we also want to urge them to take the pledge on <a href="https://www.firstpost.com/diageoroadtosafety">https://www.firstpost.com/diageoroadtosafety</a> that <b>“I Will Never Drink & Drive”</b> .</p>
							<h5>SOLUTION:</h5>
							<p>Our solution included the doodles, creative copies and factual posts for promotion. On the other hand, the media partners of Diageo India were up on their toes to promote the initiative on their social media handles. Therefore, we opted for quirky doodles which will uniquely promote all road safety minutes. Our entire promotion went with <b>#DriveResponsibly</b>.</p>
							<h5>EXECUTION:</h5>
							<p>With our <b>#DriveResponsibly</b>, we made different creative and doodles in which we focused on-</p>
